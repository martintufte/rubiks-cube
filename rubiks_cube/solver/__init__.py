LOGGER = logging.getLogger(__name__)


def _convert_solution_to_rotated_frame(
    solution: MoveSequence,
    normal_rotation_moves: list[str],
    conjugation_map: dict[tuple[str, str], str],
    inverse_map: dict[str, str],
) -> MoveSequence:
    """Convert a solution from the original frame to the rotated frame.

    The scramble has a net rotation g = r1 ∘ r2 ∘ ... ∘ rn. Solution moves found in
    the de-oriented (original) frame are converted to the rotated frame by applying
    conjugation with g^-1. Using the composition identity
    conjugate(M, g^-1) = conjugate(M, rn^-1 ... r1^-1), this is computed iteratively
    by applying conjugation_map[(M, ri^-1)] for each ri in the original order.

    Args:
        solution: Solution move sequence in the original (de-oriented) frame.
        normal_rotation_moves: Rotation moves from the scramble normal part, in order.
        conjugation_map: Map (move, rotation) -> conjugated move from MoveMeta.
        inverse_map: Map move -> inverse move from MoveMeta.

    Returns:
        MoveSequence: Solution expressed in the rotated frame.
    """

    def convert_move(move: str) -> str:
        converted = move
        for rot in normal_rotation_moves:
            inv_rot = inverse_map[rot]
            if (converted, inv_rot) in conjugation_map:
                converted = conjugation_map[(converted, inv_rot)]
        return converted

    return MoveSequence(
        normal=[convert_move(move) for move in solution.normal],
        inverse=[convert_move(move) for move in solution.inverse],
    )


def solve_pattern(
    sequence: MoveSequence,
    move_meta: MoveMeta,
    generator: MoveGenerator | None = None,
    algorithms: list[MoveAlgorithm] | None = None,
    goal_sequence: MoveSequence | None = None,
    goal: Goal = Goal.solved,
    variants: list[Variant] | None = None,
    min_search_depth: int = 0,
    max_search_depth: int = 10,
    max_solutions: int = 1,
    solve_strategy: SolveStrategy = SolveStrategy.normal,
    max_time: float = 60.0,
) -> SearchSummary:
    """Solve a Rubik's cube goal pattern.

    High-level functionality:

    1. Initialize:
        - Use the generator to get the action space, which is the set of all possible moves in a
        permutation. It expands the action space to include the actions that are generated by each
        sequence, if the sequence is in the list of standard moves. I.e. if the sequence is "U",
        then the action space will include "U", "U2", and "U'". The action space can also include
        algorithms, which are predefined sequences of moves.
        - Get the cube pattern for the step. This is the "colored" cube to match. The pattern
        can be seen as a way to color the facelets on the cube, and allows for solving steps such as
        orientations, permutations and CFOP steps such as cross, F2L, and the full cube.
        - Find the initial permutation by applying the sequence. If the goal sequence is provided,
        then apply the inverted goal sequence. This allows the solver to find the shortest path
        between two permutations (i.e. the matching permutation does not have to be the identity).

    2. Setup the bidirectional solver:
        - Use the actions and pattern to optimize which facelet indexes to use, removing redundant
       actions, indexes that are bijections of each other or always remains solved.
       This reduces the sizes of the permutations and the search space.
       - Use optimal data types to store the pattern, reducing memory usage.
       - Optimize the move order based on the action space.

    3. Solve:
        - Use the solver to find the shortest sequence of moves that transforms the
        initial permutation into a permutation that matches the pattern. The solver explores the
        state space from both the initial permutation under the pattern and the goal pattern
        simultaneously, meeting in the middle.

    Args:
        sequence (MoveSequence): Sequence to scramble the cube.
        move_meta (MoveMeta): Meta information about moves.
        generator (MoveGenerator | None, optional): Generator for actions at each step.
            Defaults to None.
        algorithms (list[MoveAlgorithm] | None, optional):
            List of algorithms to include in the action space.
        goal_sequence (MoveSequence | None, optional): Sequence to scramble the goal permutation.
        goal (Goal | None, optional): Goal to solve. Defaults to Goal.Solved.
        min_search_depth (int, optional): Minimum search depth. Defaults to 0.
        max_search_depth (int, optional): Maximum search depth. Defaults to 10.
        max_solutions (int, optional): Maximum number of solutions. Defaults to 1.
        solve_strategy (SolveStrategy, optional): Search strategy (normal, inverse, both).
            Defaults to SolveStrategy.normal.
        max_time (float, optional): Maximum time in seconds. Defaults to 60.0.

    Returns:
        SearchSummary: Summary of the search.
    """
    LOGGER.info(f"Solving with goal '{goal.name}' and strategy '{solve_strategy.value}'..")
    LOGGER.debug(f"Sequence: {sequence}")

    actions = get_actions(move_meta=move_meta, generator=generator, algorithms=algorithms)
    pattern = get_patterns(cube_size=move_meta.cube_size).get(goal)
    assert pattern is not None

    # Detect rotation moves in the normal sequence to enable solving from a rotated state.
    # If present, the permutation is de-oriented (rotations stripped) and solutions are
    # mapped back to the rotated frame via conjugation after solving.
    normal_rotation_moves = [move for move in sequence.normal if move in move_meta.rotation_moves]

    has_rotation = bool(normal_rotation_moves)

    if goal_sequence is not None:
        inverse_goal_permutation = get_rubiks_cube_permutation(
            sequence=goal_sequence,
            move_meta=move_meta,
            invert_after=True,
        )
    else:
        inverse_goal_permutation = None

    permutation = get_rubiks_cube_permutation(
        sequence=sequence,
        initial_permutation=inverse_goal_permutation,
        move_meta=move_meta,
        orientate_after=has_rotation,
    )

    if solve_strategy is SolveStrategy.normal:
        search_sides = [SearchSide.normal]
    elif solve_strategy is SolveStrategy.inverse:
        search_sides = [SearchSide.inverse]
    else:
        search_sides = [SearchSide.normal, SearchSide.inverse]

    all_solutions: list[MoveSequence] = []
    status = Status.Failure
    total_walltime = 0.0

    if variants is not None and len(variants) > 0:
        if not all(variant in pattern.variants for variant in variants):
            raise ValueError(f"Got unknown variants {variants} for goal {goal}")
    else:
        variants = list(pattern.variants)

    for side in search_sides:
        for variant in variants:
            remaining_time = max_time - total_walltime
            if remaining_time <= 0:
                break

            # Rotate permutation and pattern to the canonical variant frame.
            # All variants in a group share the same canonical pattern, enabling
            # the inverse frontier to be persisted across variants later.
            if variant is Variant.none:
                r_v_moves: list[str] = []
                canonical_variant = variant
            else:
                variant_group = find_variant_group(variant)
                canonical_variant = next(k for k, v in variant_group.items() if not v)
                r_v_moves = variant_group[variant]

            if r_v_moves:
                LOGGER.debug(
                    f"Variant '{variant.value}' -> canonical '{canonical_variant.value}' "
                    f"via rotation {r_v_moves}"
                )
                r_v_perm = get_rubiks_cube_permutation(
                    sequence=MoveSequence(normal=r_v_moves),
                    move_meta=move_meta,
                )
                solve_permutation = conjugate(permutation, r_v_perm)
            else:
                solve_permutation = permutation

            solver = BidirectionalSolver.from_actions_and_pattern(
                actions=actions,
                pattern=pattern[canonical_variant],
                validator=pattern.validator,
                optimize_indices=True,
                debug=True,
            )

            pattern_summary = solver.search(
                permutation=solve_permutation,
                max_solutions=max_solutions,
                min_search_depth=min_search_depth,
                max_search_depth=max_search_depth,
                max_time=remaining_time,
                side=side,
            )

            total_walltime += pattern_summary.walltime
            if pattern_summary.status is Status.Failure:
                continue

            status = Status.Success
            if len(pattern_summary.solutions) == 0:
                continue

            # Convert solutions from canonical frame back to the variant frame.
            variant_solutions = pattern_summary.solutions
            if r_v_moves:
                variant_solutions = [
                    _convert_solution_to_rotated_frame(
                        solution=solution,
                        normal_rotation_moves=r_v_moves,
                        conjugation_map=move_meta.conjugation_map,
                        inverse_map=move_meta.inverse_map,
                    )
                    for solution in variant_solutions
                ]

            all_solutions.extend(variant_solutions)

    unique_solutions = {str(solution): solution for solution in all_solutions}
    solutions = sorted(
        unique_solutions.values(),
        key=lambda solution: (len(solution), str(solution)),
    )[:max_solutions]

    # Convert solutions from the de-oriented (original) frame back to the rotated frame
    if has_rotation:
        solutions = [
            _convert_solution_to_rotated_frame(
                solution=solution,
                normal_rotation_moves=normal_rotation_moves,
                conjugation_map=move_meta.conjugation_map,
                inverse_map=move_meta.inverse_map,
            )
            for solution in solutions
        ]

    search_summary = SearchSummary(
        solutions=solutions,
        walltime=total_walltime,
        status=status,
    )

    LOGGER.info(
        f"Solver found {len(search_summary.solutions)} solutions in {search_summary.walltime:.2f}s"
    )

    return search_summary
